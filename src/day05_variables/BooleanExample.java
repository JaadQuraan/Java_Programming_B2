package day05_variables;

public class BooleanExample {
    public static void main(String [] args) {

        boolean isSleepy = true;
        boolean isHungry = false;
        boolean isWeekend = false;
        boolean isWeekday = true;
        //Will only hold true or false

        System.out.println("isSleepy = " + isSleepy);
        System.out.println("isHungry = " + isHungry);
        System.out.println("It is a weekend: " + isWeekend);
        System.out.println("It is a weekday: " + isWeekday);

    }
}
