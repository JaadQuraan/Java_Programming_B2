package day05_variables;

public class ConateExample {
    public static void main(String [] args) {
        System.out.println(4 + 5); //9
        //Whole numbers java takes as int by default
        System.out.println(4 + 5 + "Hello"); //9Hello
            // int + int + String
            //     int + String
            //        String
        System.out.println("Hello" + 4 + 9); //Hello49
        //                  String + int + int
        //                  String + int
        //                      String
    }
}
